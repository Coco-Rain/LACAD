import cadquery as cq

result = (
cq.Workplane()
.tag('base')
.box(101, 22, 62, centered=(True, False, True))
.workplaneFromTagged('base')
.center(0, 22)
.tag('lower')
.box(98, 40, 62, centered=(True, False, True))
.workplaneFromTagged('lower')
.center(0, 40)
.vLine(-1)
.hLine(32)
.vLine(1)
.lineTo(15, 15)
.hLineTo(0)
.mirrorY()
.extrude(31, both=True)
)
cq.exporters.export(result, 'GT.stl')